package QQbean;

public class QQfriendtable {

	private int friendid;
	private int fQQid;
	private int mQQid;
	private String groupname;
	public int getFriendid() {
		return friendid;
	}
	public void setFriendid(int friendid) {
		this.friendid = friendid;
	}
	public int getFQQid() {
		return fQQid;
	}
	public void setFQQid(int qid) {
		fQQid = qid;
	}
	public int getMQQid() {
		return mQQid;
	}
	public void setMQQid(int qid) {
		mQQid = qid;
	}
	public String getGroupname() {
		return groupname;
	}
	public void setGroupname(String groupname) {
		this.groupname = groupname;
	}
}
