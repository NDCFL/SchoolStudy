public class suan 
{
	public static void main(String[] args) 
	{
		int a=20;
		int b=5;
		
		/*
			20化成二进制为=16+4=10100
			5化为二进制为=4+1=101；
			10100化成32位
			00000000，00000000，00000000，00010100
			------------------------------------------
			101化为32位
			00000000，00000000，00000000，00000101

			&逻辑与操作：上下都为1时才为1
			00000000，00000000，00000000，00010100----20
			00000000，00000000，00000000，00000101----5
			---------------------------------------
			00000000，00000000，00000000，00000100----4

			&逻辑或操作：有1为1
			00000000，00000000，00000000，00010100----20
			00000000，00000000，00000000，00000101----5
			---------------------------------------
			00000000，00000000，00000000，00010101----16+4+1=21

			&逻辑异或操作：有1为1
			00000000，00000000，00000000，00010100----20
			

		*/
		System.out.println("与操作a&b="+(a&b));
		System.out.println("与操作a|b="+(a|b));
		System.out.println("与操作a^b="+(a^b));
		System.out.println("取反b="+(~b));
	}
}
